def 숫자형():

    # 정수형
    a = 123
    print(a); print(type(a))

    # 실수형
    a = 1.2
    print(a); print(type(a))

    # 8진수 (8진수와 16진수는 잘 안쓰임임)
    a = 0o177
    print(a); print(type(a))

    # 사칙연산
    a = 7
    b = 3
    print(a+b)
    print(a-b)
    print(a*b)
    print(a/b)
    print(a**b)
    print(a//b)

    # 복합연산자
    a=1
    a += 1
    print(a)

